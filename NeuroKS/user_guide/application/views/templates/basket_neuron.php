                                    Compared with the granule cell, the basket cell is quite complicated. It receives synapses from parallel fibers and to a limited extent from climbing fibers, but it devotes its entire axonal output to the Purkinje cells with the possible exception of a few contacts on other basket cells and Golgi cells. A large number of widely dispersed parallel fibers converge on its dendrites, but its axon sends divergent impulses to only a small number of Purkinje cells. The basket cell has a roughly pyramidal or ovoid shape, and it lies in the lower third of the molecular layer with its long axis parallel to the Purkinje cell layer in the sagittal plane. In Nissl preparations little more can be seen of it than its triangular or oval cell body, about 10 micrometers long....Ramon y Cajal (1888a and b) was the first to discover the characteristic terminal plexus elaborated around the Purkinje cell body by the axons of these cells. He named this formation the pericellular nest (nid pericellulaire). His finding was quickly confirmed by Kolliker (1890), who referred to the same structure as a "fiber basket" (Faserkorb), and to the cells that gave rise to it as "basket cells" (Korbzellen).
