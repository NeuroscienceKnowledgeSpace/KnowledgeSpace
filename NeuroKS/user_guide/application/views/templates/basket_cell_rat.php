                                    Within the upper part of the Ju, there is a third neuronal type, the basket cell (BC), which accounts for about 8% of the neurons observed in the nucleus. The BCs correspond to the second type of short-axon neurons observed in the Ju. The soma of these neurons is ovoid, measuring from 18 to 22 micrometers in the longest axis. The perikaryon gives rise to a dorsal dendrite and one or two lateral dendrites, which in both cases ramify quickly into long secondary shafts and rarely provide short tertiary branches. Four additional features characterize the dendrites of BC: They follow a straight trajectory; they display varicosities; they bear a few dendritic spines; and at least one set of dendrites is meshed into the axonal bundles surrounding the Ju. In every cell, the axon descends from the lower pole of the soma, and shortly afterward gives rise to a number of recurrent fibers adopting the form of typical axonal arcades that return to the dendritic field proper (Fig. 4A,B). These recurrent axonal branches are in apposition to the adjacent somata and/or to proximal dendrites of bipolar and pyramidal neurons. In three of the sampled neurons, the axon proceeds farther ventrally, issuing additional horizontal collaterals along its way. Both the descending axon and the horizontal collaterals appear to terminate as en passage and terminal boutons on the shafts and spines of bipolar and triangular neurons.
