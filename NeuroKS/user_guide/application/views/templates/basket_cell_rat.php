                               <td>
                                    The core of the Ov exhibits a neuron type that strongly resembles the basket cells described by Ramon y Cajal (1904) in the mammalian neocortex and by Larriva-Sahd (2004) in the adult rat Ju of the BST. BCs have a small (15–20 micrometers), triangular or pear-shaped soma that gives rise to two or four primary divergent dendrites that divide sequentially into secondary and terminal (i.e., tertiary) branches. Three characteristics are consistently observed in BC dendrites: 1) shafts hold distinct varicosities, 2) shafts are virtually devoid of spines, and 3) the dendritic field they form is relatively small (150–180 micrometers). The axon arises from the soma, and it is usually directed ventrally. Typically, the axon gives rise to transverse collaterals, which curve in the form of arcades that return toward the neuron’s own dendritic tree (see Jones and Hendry, 1984). Occasional straight collaterals, usually from the main axon, descend farther ventrally. An additional feature of certain BCs is that their recurrent collaterals appear to contact the dendritic processes of the cell, resembling that documented by Fairen et al. (1982) for the homologous neurons in the isocortex, the so-called autapses.
